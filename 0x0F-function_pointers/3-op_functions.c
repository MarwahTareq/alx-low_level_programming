#include "3-calc.h"
/**
 * op_add - calculate the sum of two integer
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - calculate the diffrent of two integer
 * @a: first integer
 * @b: second integer
 *
 * Return: diffrence of a and b
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - calculate the dividition of two integers
 * @a: first integer
 * @b: second integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - calculate the remainders
 * @a: first integer
 * @b: second integer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculate the remainders
 * @a: first integer
 * @b: second integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
